    static readonly Regex _regex = new(@"([\u00C0-\u1FFF])", RegexOptions.Compiled);

    /// <summary>
    /// Replace all non latin-1 characters in the string.
    /// </summary>
    /// <param name="str">Input string with non latin-1 characters.</param>
    /// <returns>Output string without the non latin-1 characters.</returns>
    public static string ReplaceAll(string str)
    {
        return _regex.Replace(str, match =>
        {
            var character = match.Groups[1].Value;
            return _characterReplacementMap.ContainsKey(character) ? _characterReplacementMap[character] : character;
        });
    }
